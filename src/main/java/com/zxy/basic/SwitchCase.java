package com.zxy.basic;

public class SwitchCase {
    public void doSwitchCase() {
        int condition = 2;
        switch (condition) {
            case 1:
                System.out.println("变量值为1");
                break;
            case 2:
                System.out.println("变量值为2");
                break;
            default:
                System.out.println("未知");
                break;

        }
    }
}
