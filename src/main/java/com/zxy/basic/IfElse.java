package com.zxy.basic;

public class IfElse {
    public void doIfElse() {
        int x = 30;

        if( x == 10 ){
            System.out.print("Value of X is 10\n");
        }else if( x == 20 ){
            System.out.print("Value of X is 20\n");
        }else if( x == 30 ){
            System.out.print("Value of X is 30\n");
        }else{
            System.out.print("这是 else 语句");
        }
    }
}
